#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size)
	{
		if (*(unsigned char *)s == (unsigned char) c)
			return ((void *)(s));
		i++;
		s++;
	}
	return (NULL);
}
